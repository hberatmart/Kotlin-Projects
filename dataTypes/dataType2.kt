
fun main(){
//Byte: -128 <= Byte <= 127
    var myByte: Byte = 100
    println("Byte: $myByte")

//Short: -32768 <= Short <= 32767
    var myShort: Short = 10000
    println("Short: $myShort")

//Int: -2147483648 <= Int <= 2147483647
    var myInt: Int = 1000000000
    println("Int: $myInt")

//Long: -9223372036854775808 <= Long <= 9223372036854775807
    var myLong: Long = 10000000000000 // or 1000000000L
    println("Long: $myLong")

//Float: 3.4e-038 <= Float <= 3.4e+038
    var myNum: Float = 5.75f
    println("Float: $myNum")

//Double: 1.7e-308 <= Double <= 1.7e+038
    var myNum2: Double = 19.99
    println("Double: $myNum2")
}


